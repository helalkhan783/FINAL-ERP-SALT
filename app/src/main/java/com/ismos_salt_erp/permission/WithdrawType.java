package com.ismos_salt_erp.permission;

public class WithdrawType {
    String type;
    String value;

    public WithdrawType(String type, String value) {
        this.type = type;
        this.value = value;
    }

    public String getType() {
        return type;
    }
    public String getValue() {
        return value;
    }
 }
